/*  1.用户输入的大小写字母判断问题： 代码中判断第三个字符是否小于等于 'z'，但
 这可能会造成误判，因为这里没有考虑大写字母或其他字符的情况。如果用户输入的是大
 写字母或其他字符，可能会产生不正确的结果。可以使用 std::islower() 函数来准确判
 断一个字符是否为小写字母。
    2.字符大小写转换方法问题： 代码中使用 s[2] = s[2] - 32; 进行字符转换，这依赖
 于 ASCII 码的差值，虽然通常可以工作，但这种方式更适合直接处理字符。对于字符
 串中的某个字符，使用 std::toupper() 函数会更清晰且可移植，它会考虑不同字符集
 的情况。
*/

#include <iostream>   // 包含输入输出流库
#include <string>     // 包含字符串库
#include <cctype>     // 包含字符处理函数的头文件

using std::string;    // 使用string类型的简化命名
using std::cout;      // 使用cout对象的简化命名
using std::cin;       // 使用cin对象的简化命名

int main() {
    string s = "abcde"; // 声明并初始化一个string类型的变量s为"abcde"
    cout <<"原本的S："<< s << "\n";  // 输出变量s的值，即"abcde"
    cout << "输入S：";   // 提示用户输入信息
    cin >> s;           // 从标准输入读取用户输入的值并赋给变量s
    cout << s << "\n";  // 输出用户输入的值

    cout << s.length() << "\n"; // 输出变量s的长度（字符个数）

    if (s.length() >= 3 && std::islower(s[2])) {
        // 如果s的长度大于等于3且第三个字符是小写字母
        // 则将第三个字符转换为大写字母形式
        s[2] = std::toupper(s[2]); // 使用字符转换函数进行大小写转换
    }
    cout << s; // 输出经过可能的字符转换后的字符串

    return 0; // 返回0，表示程序顺利执行完毕
}
